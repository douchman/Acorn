package Minigame.Ladder;

public class Dot2 {
	private int X, Y;
	private int linkedX, linkedY;
	
	public Dot2(int X, int Y) {
		this.X = X;
		this.Y = Y;
	}
	
	public void setLinkedXY(int linkedX, int linkedY) {
		this.linkedX = linkedX;
		this.linkedY = linkedY;
	}
	public int getX() {
		return X;
	}

	public void setX(int x) {
		X = x;
	}

	public int getY() {
		return Y;
	}

	public void setY(int y) {
		Y = y;
	}

	public int getLinkedX() {
		return linkedX;
	}

	public int getLinkedY() {
		return linkedY;
	}



	
	

	
	
	
	

	
}
